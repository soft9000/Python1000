print()

print("Type 1 - range(5)")
zrange = range(5)
for zint in zrange:
    print(zint)
print()


print("Type 2 - range(1, 5)")
zrange = range(1, 5)
for zint in zrange:
    print(zint)

print()

print("Type 3 - range(1, 5, 2)")
zrange = range(1, 5, 2)
for zint in zrange:
    print(zint)

print()

